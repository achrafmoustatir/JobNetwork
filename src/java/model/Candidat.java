
package model;


public class Candidat {
    
    private int idC;
    private String Nomc;
    private String Prenomc;
    private String EmailC;
    private String MPassC;
    private String DatenC;
    private String AdresseC;
    private String PhotoC;
    private String sexeC;
    private String TelC;

    public int getIdC() {
        return idC;
    }
    public void setIdC(int idC) {
        this.idC = idC;
    }

    public String getNomc() {
        return Nomc;
    }

    public void setNomc(String Nomc) {
        this.Nomc = Nomc;
    }

    public String getPrenomc() {
        return Prenomc;
    }

    public void setPrenomc(String Prenomc) {
        this.Prenomc = Prenomc;
    }

    public String getEmailC() {
        return EmailC;
    }

    public void setEmailC(String EmailC) {
        this.EmailC = EmailC;
    }

    public String getMPassC() {
        return MPassC;
    }
 
    public void setMPassC(String MPassC) {
        this.MPassC = MPassC;
    }

    public String getDatenC() {
        return DatenC;
    }

    public void setDatenC(String DatenC) {
        this.DatenC = DatenC;
    }

    public String getAdresseC() {
        return AdresseC;
    }

    public void setAdresseC(String AdresseC) {
        this.AdresseC = AdresseC;
    }

    public String getPhotoC() {
        return PhotoC;
    }

    public void setPhotoC(String PhotoC) {
        this.PhotoC = PhotoC;
    }

    public String getSexeC() {
        return sexeC;
    }

    public void setSexeC(String sexeC) {
        this.sexeC = sexeC;
    }

    public String getTelC() {
        return TelC;
    }

    public void setTelC(String TelC) {
        this.TelC = TelC;
    }

}
